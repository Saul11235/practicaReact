// Función para mostrar el spinner
function showSpinner() {
  var spinner = document.getElementById('overlay');
  spinner.style.display = 'block';
}

// Función para ocultar el spinner
function hideSpinner() {
  var spinner = document.getElementById('overlay');
  spinner.style.display = 'none';
}

